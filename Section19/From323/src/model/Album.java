package model;

public class Album {
    private int id;
    private String name;
    private int artisdId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getArtisdId() {
        return artisdId;
    }

    public void setArtisdId(int artisdId) {
        this.artisdId = artisdId;
    }
}
